     private void cadastrarUsuario() throws java.sql.SQLException {
        String username = txtUsername.getText();
        String senha = new String(txtSenha.getPassword());

        if (username.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            try (Connection conexao = DriverManager.getConnection(urlBanco, usuario, this.senha)) {
                String sql = "INSERT INTO usuarios (username, senha) VALUES (?, ?)";
                try (PreparedStatement stmt = conexao.prepareStatement(sql)) {
                    stmt.setString(1, username);
                    stmt.setString(2, senha);
                    int linhasAfetadas = stmt.executeUpdate();

                    if (linhasAfetadas > 0) {
                        JOptionPane.showMessageDialog(this, "Usu√°rio cadastrado com sucesso.", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao cadastrar usu√°rio.", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }        }
    }
  

