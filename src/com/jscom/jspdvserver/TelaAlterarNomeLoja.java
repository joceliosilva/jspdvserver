    private void btnSalvarImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarImagemActionPerformed
            String novoNomeLoja = txtNomeLoja.getText();

    if (novoNomeLoja != null && !novoNomeLoja.isEmpty()) {
        try {
            java.sql.Connection conexao = DriverManager.getConnection(urlBanco, usuario, senha);
            String sql = "UPDATE loja SET nome = ? WHERE id = 1"; // Supondo que a loja tenha um ID de 1
            PreparedStatement stmt = conexao.prepareStatement(sql);
            stmt.setString(1, novoNomeLoja);
            stmt.executeUpdate();
            stmt.close();
            conexao.close();

            // O nome da loja foi atualizado no banco de dados
            JOptionPane.showMessageDialog(this, "Nome da loja atualizado com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar o nome da loja: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "O campo do nome da loja est√° vazio.");
    }

    }//GEN-LAST:event_btnSalvarImagemActionPerformed

