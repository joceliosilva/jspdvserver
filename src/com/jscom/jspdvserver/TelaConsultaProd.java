    private void buscarProdutoPorNome(String termoBusca) {
    DefaultListModel<String> model = new DefaultListModel<>();

    for (Produto produto : listaProdutos) {
        if (produto.getNome().toLowerCase().contains(termoBusca.toLowerCase())) {
            model.addElement(" " + produto.getId()+
                              " | " + produto.getNome() +
                              " - R$ " + produto.getPreco());
        }
    }

    listaProdutosJList.setModel(model);
}

  
